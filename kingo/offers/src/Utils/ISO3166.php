    /**
     * @param $geo
     * @return mixed|string
     */
    public function getAlpha3($geo)
    {
        $geo = strtoupper($geo);
        if (strlen($geo) == 2) {
            return isset(self::$alpha2To3[$geo]) ? self::$alpha2To3[$geo] : '';
        } else {
            return $geo;
        }
    }

    /**
     * @param $geo
     * @return mixed|string
     */
    public function getAlpha2($geo)
    {
        $geo = strtoupper($geo);
        if (strlen($geo) == 3) {
            return isset(self::$alpha3To2[$geo]) ? self::$alpha3To2[$geo] : '';
        } else {
            return $geo;
        }
    }


    /**
     * @param $geo
     * @return mixed|string
     */
    public function getCN($geo)
    {
        $geo = strtoupper($geo);
        if (strlen($geo) == 2) {
            return isset(self::$alpha2ToCN[$geo]) ? self::$alpha2ToCN[$geo] : '';
        } elseif (strlen($geo) == 3) {
            return isset(self::$alpha3ToCN[$geo]) ? self::$alpha3ToCN[$geo] : '';
        } else {
            return '';
        }
    }
